    #[inline(always)]
    fn lt(&mut self, k: isize) -> Option<Token> {
        if k == 0 {
            return None;
        }
        if k < 0 {
            return self.lb(-k);
        }
        self.lt_inner(k)
    }

    #[inline]
    fn get(&self, index: isize) -> &<Self::TF as TokenFactory<'input>>::Tok {
        self.base.get(index)
    }

